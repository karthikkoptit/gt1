package com.nutrisystem.orange.java.range;


/**
 * @author Wei Gao
 * 
 */
public class IntegerRange {
    private int min;
    private int max;

    public IntegerRange() {
    }

    public int getMin() {
        return min;
    }

    public int getMax() {
        return max;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public void setMax(int max) {
        this.max = max;
    }
}
